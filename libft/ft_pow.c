#include "libft.h"

int		ft_pow(int nbr, int pow)
{
	int i;

	i = 0;
	if (pow < 0)
		return (0);
	else if (pow == 0)
		return (1);
	while (i < pow - 1)
	{
		nbr = nbr * nbr;
		i++;
	}
	return (nbr);
}
